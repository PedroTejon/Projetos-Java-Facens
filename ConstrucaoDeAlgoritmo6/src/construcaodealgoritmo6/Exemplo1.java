package construcaodealgoritmo6;

public class Exemplo1 {

    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        int x = 1;
        while (x <= 10) {
            System.out.println(x);
            x++;
        }
    }
}
