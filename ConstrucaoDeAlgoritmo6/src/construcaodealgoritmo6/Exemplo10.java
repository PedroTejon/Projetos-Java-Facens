package construcaodealgoritmo6;

public class Exemplo10 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print(i + "/" + j + " ");
            }
            System.out.println("");
        }
    }
}
