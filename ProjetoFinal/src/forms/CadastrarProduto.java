    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        prodAt.setDescricao(txtDescricao.getText());
        prodAt.setQuantidade(Integer.parseInt(txtQntd.getText()));
        prodAt.setValorUnitario(Double.parseDouble(txtValor.getText()));
        prodAt.setCategoriaProduto(categoriaProdutoDAO.selecionarPorCodigo(Long.parseLong(txtID.getText())));
        
        System.out.println(editando);
        if (editando)
            produtoDAO.editar(prodAt);
        else 
            produtoDAO.inserir(prodAt);
        
        JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
        
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

