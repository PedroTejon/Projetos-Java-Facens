    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        this.cursoCadastrado = new Curso();
        
        this.cursoCadastrado.setNome(txtNome.getText());
        this.cursoCadastrado.setDuracao(slDuracao.getValue());
        this.cursoCadastrado.setCodigo(UUID.randomUUID());
        
        JOptionPane.showMessageDialog(this, "Salvo com sucesso", "Atenção", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        if (cursoCadastrado != null)
            JOptionPane.showMessageDialog(this, cursoCadastrado.imprimir());
        else
            JOptionPane.showMessageDialog(this, "Nenhum curso cadastrado ainda.", "Erro", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnMostrarActionPerformed

